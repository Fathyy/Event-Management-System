<?php
require_once 'config.php';

$db_conn = mysqli_connect($host, $user, $pass, $dbName);

// execute a database query
function db_query($sql){
    global $db_conn;
    $result = mysqli_query($db_conn, $sql);
    return $result;
}

// return affected rows
function affected_rows(){
    global $db_conn;
    return mysqli_affected_rows($db_conn);
}

// fetch a row as a numeric array
function fetch_array($result, $result_type = MYSQLI_NUM){
    return mysqli_fetch_array($result, $result_type);
}

// fetch a row as an associative array
function fetch_assoc($result){
    return mysqli_fetch_assoc($result);
}

// Fetch the next row of a result set as an enumerated array
function fetch_row($result){
    return mysqli_fetch_row($result);
}

// free the memory associated with the result.
function free_result($result){
    return mysqli_free_result($result);
}

// return number of rows in a result set
function num_rows($result){
    return mysqli_num_rows($result);
}

// change the database name
function change_db($dbName){
    return mysqli_select_db($dbName);
}

// return autogenerated id from the last query
function inserted_id(){
    return mysqli_insert_id();
}
?>